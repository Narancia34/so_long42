#include "libftprintf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
		return (ft_printf("(null)"));
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
